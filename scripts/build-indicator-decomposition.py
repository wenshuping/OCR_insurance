def style_header(ws, row_idx, col_count):
    for col in range(1, col_count + 1):
        cell = ws.cell(row=row_idx, column=col)
        cell.fill = HEADER_FILL
        cell.font = HEADER_FONT
        cell.alignment = CENTER_ALIGN
        cell.border = THIN_BORDER


def style_data(ws, start_row, end_row, col_count):
    for r in range(start_row, end_row + 1):
        for c in range(1, col_count + 1):
            cell = ws.cell(row=r, column=c)
            cell.font = DATA_FONT
            cell.alignment = TOP_ALIGN
            cell.border = THIN_BORDER


def set_col_widths(ws, widths):
    for idx, w in enumerate(widths):
        ws.column_dimensions[chr(65 + idx) if idx < 26 else chr(64 + idx // 26) + chr(65 + idx % 26)].width = w


def add_sheet_with_data(wb, title, headers, rows, widths):
    ws = wb.create_sheet(title=title)
    ws.append(headers)
    for row in rows:
        ws.append(row)
    style_header(ws, 1, len(headers))
    if rows:
        style_data(ws, 2, 1 + len(rows), len(headers))
    set_col_widths(ws, widths)
    ws.freeze_panes = "A2"
    return ws


